## Poll: Iran

### Awareness and Support

The conflict with Iran is dominating news coverage. Eight-in-10 Americans say they are hearing about the conflict in the Middle East and the U.S. striking Iran (80%), including 55% who say they are hearing "a lot."

By nine points, Americans oppose the military operation against Iran (40% support – 49% oppose). Majorities of Democrats and independents oppose the military operation (74% and 52% oppose, respectively), while Republicans are more supportive (70% support). MAGA Republicans are significantly more likely to support the operation (net +73; 84% support – 11% oppose) compared to non-MAGA Republicans (net +8; 48% support – 40% oppose).

### Why Trump Launched the War

When picking the top three reasons President Trump launched the military operation against Iran, Americans are most likely to say he wants to get access to oil and other resources in the region (40%) and he wants to use conflict to distract from other issues like the Epstein files (40%). Slightly less believe he actually felt Iran''s nuclear and missile programs posed an imminent threat (38%).

As Trump''s reasons for the war are questioned, by 18 points, Americans think the military operation against Iran has made the world less safe (28% safer – 46% less safe).

One-in-five Americans who voted for Trump in 2024 continue to say they regret their vote (19%). When citing why they regret their vote, phrases like "war," "promises," and "prices" dominate their reasons.

### Lack of Clear Timeline and Goals for the War

A majority of Americans believe the U.S. conflict with Iran will last months or longer (53%), and 30% believe it will last a year or longer. Two-in-three are concerned that the U.S. will become bogged down in a prolonged conflict with Iran (67%), including 84% of Democrats, 74% of independents, and 47% of Republicans.

### Concerns Over the Cost of War

By 20 points, Americans oppose Congress providing an additional $50 billion in funding (34% support – 54% oppose). Top concerns:

- It will raise oil and gas prices (34%)
- It is another conflict that will lead to billions being spent abroad instead of helping Americans at home (29%)
- It has resulted in the deaths of innocent civilians, including children (27%)
- It puts U.S. troops unnecessarily in harm''s way, with 13 troops already killed (27%)

As **85% already believe the cost of gas is rising**, Americans continue to want Trump and Congress to prioritize inflation and the cost of living (58%).

### Messaging on Trump''s Actions in Iran

Most concerning messages about the conflict with Iran:

- *"Trump cut programs people depend on, like Medicaid and SNAP, and is using that money to fund this multi-billion dollar war with Iran."* **(61% concerning, including 47% "extremely" concerning).**
- *"We can''t afford Trump''s war with Iran. In the first two days of the conflict alone, the U.S. spent nearly $6 billion on the war."* **(62% concerning, including 45% "extremely" concerning).**

