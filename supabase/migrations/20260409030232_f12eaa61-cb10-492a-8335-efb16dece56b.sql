## Poll: Tariffs

### Tariff Favorability and Support

Tariffs remain overwhelmingly underwater, with 60% of Americans viewing them unfavorably and only 30% holding favorable views as of January.

A similar margin opposes President Trump''s tariff plan, with 55% opposing and only 38% in support. Support is largely partisan, opposed by 88% of Democrats, 55% of independents, and 21% of Republicans. Among Republicans, MAGA Republicans are 35 points more likely to support (86% MAGA – 51% non-MAGA).

- Across income groups, Americans oppose Trump''s tariff plan, including among those who identify as "middle class" (43% support – 51% oppose).
- Americans earning less than $50,000 a year have become 11 points more negative on Trump''s tariff plan since September, shifting from -22 to -33 in January.

### Tariffs and Costs

A majority across partisanship believe tariffs have led to an increase in their costs (69%), including 88% of Democrats, 65% of independents, and 52% of Republicans. Only 10% say tariffs have made their costs go down.

Majorities disapprove of President Trump''s handling of inflation and the cost of living (58% disapprove – 37% approve).

### Message Guidance

Despite the decision from the Supreme Court, many of the Republican tariffs will continue. It''s crucial to continue telling the stories of how these tariffs are raising costs for things people buy every day.

**Tips for communicating on the negative impacts of tariffs:**

- **DO** talk about how tariffs are raising the cost of everyday items families need
- **DO** connect tariffs to specific price increases people are experiencing
- **DO** highlight that Trump and Republicans in Congress backed these tariffs
- **DON''T** use abstract economic arguments – focus on real costs
- **DON''T** assume people understand tariff mechanics – explain the impact simply

