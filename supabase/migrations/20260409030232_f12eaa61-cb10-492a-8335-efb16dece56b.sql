## Poll: ICE

### Views Of Trump''s Handling Of Immigration And Mass Deportations

Americans'' approval of President Trump''s handling of immigration continues to fall to a net -12. Just two months ago, before Renee Good and Alex Pretti were killed by federal immigration officials, Trump''s net approval on immigration was at -7. Net approval is notably low among women (net -17), people between 18 and 34 (net -21), people of color (net -40), and independents (net -27).

