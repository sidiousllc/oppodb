Nearly two-thirds (65%) say the Trump administration should mainly target deportations towards those with convictions and criminal records. However, 64% say the administration is doing the opposite, targeting all undocumented people regardless of criminal record.

Two-thirds (66%) support a balanced approach on immigration that focuses on fixing a broken system rather than strict enforcement and mass deportations (25% support). This includes a plurality (48%) of 2024 Trump voters.

